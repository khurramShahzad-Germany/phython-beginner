marks= int(input('Please enter your marks: '))
if marks >= 88 :
    print('Grade: A+')
elif marks >= 80 :
    print('Grade: A')
elif marks >= 75 :
    print('Grade: B+')
elif marks >= 70 :
    print('Grade: B')
elif marks >= 66 :
    print('Grade: C+')
elif marks >= 60 :
    print('Grade: C')
else:
    print('Grade: f')
